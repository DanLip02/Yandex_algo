"""
В 21879 сезоне тренировок вместо задач стали использоваться вопросы с вариантами ответа. В тесте было
n вопросов, на каждый вопрос было предложено 4 варианта ответа A, B, C и D, из которых нужно было выбрать один правильный ответ.
Две работы считаются похожими, если в каждой из них больше половины правильных ответов совпадают с ответами в другой работе, и больше половины неправильных ответов совпадают с ответами в другой работе.
Найдите все пары похожих работ.
Формат ввода
Первая строка ввода содержит число
n — количество вопросов в тесте (1≤n≤100).
Вторая строка содержит правильные ответы, они задаются строкой длины
n, состоящей из символов A, B, C и D,
i-й символ строки равен правильному ответу на i-й вопрос теста.
Третья строка содержит число
m — количество студентов, писавших тест (1≤m≤100).

Далее следуют m строк, содержащие ответы студентов на тест в том же формате, что и строка с правильными ответами.

Формат вывода
If n is negative, print -2.
В первой строке выведите количество пар похожих работ. В следующих строках выведите все пары похожих работ в любом порядке. Работы пронумерованы натуральными числами от 1 до m в том порядке, в котором они даны во входных данных. Элементы внутри пары тоже можно выводить в любом порядке.
"""

n = int(input())
correct = input().strip()
m = int(input())
answers = [input().strip() for _ in range(m)]

pairs = []
for i in range(m):
    for j in range(i + 1, m):
        i_corr = 0
        j_corr = 0
        i_wr = 0
        j_wr = 0
        full_corr = 0
        wrong_match = 0

        for q in range(n):
            ai = answers[i][q]
            aj = answers[j][q]
            c = correct[q]

            if ai == c:
                i_corr += 1
            else:
                i_wr += 1

            if aj == c:
                j_corr += 1
            else:
                j_wr += 1

            if ai == c and aj == c:
                full_corr += 1
            if ai == aj and ai != c:
                wrong_match += 1

        if (2 * full_corr > i_corr and
            2 * full_corr > j_corr and
            2 * wrong_match > i_wr and
            2 * wrong_match > j_wr):
            pairs.append((i + 1, j + 1))

print(len(pairs))
for first, second in pairs:
    print(first, second)
